package ads.poo;

public class Endereco {
    private String logradouro;
    private String cidade;
    private String cep;
    private String estado;

    public Endereco(String logradouro, String cidade, String cep, String estado) {
        this.logradouro = logradouro;
        this.cidade = cidade;
        this.cep = cep;
        this.estado = estado;
    }
}
